const cambiarlinea1 = () => {
    let menuHa = document.querySelector('.menuHamb');
    let changeL = document.querySelector('.change1');
    let menuOpen = false;
    menuHa.addEventListener('click', () => {
        console.log('escuchaaaaaa')
        if (!menuOpen) {

            changeL.classList.add('activeMenu');
            menuOpen = true;
        } else {
            changeL.classList.remove('activeMenu');
            menuOpen = false;
        }
    })

}
